#!/bin/bash

echo "🚀 GitHub CLI 安装和 PR 创建脚本"
echo "=================================="

# 检查是否已安装 GitHub CLI
if command -v gh &> /dev/null; then
    echo "✅ GitHub CLI 已安装"
else
    echo "📦 正在安装 GitHub CLI..."

    # 检查是否安装了 Homebrew
    if command -v brew &> /dev/null; then
        echo "使用 Homebrew 安装 GitHub CLI..."
        brew install gh
    else
        echo "⚠️  需要先安装 Homebrew"
        echo "请运行以下命令安装 Homebrew："
        echo '/bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"'
        echo ""
        echo "然后再次运行此脚本"
        exit 1
    fi
fi

# 进入项目目录
cd "/Users/lucas/claude code"

echo "🔐 正在验证 GitHub 身份..."
# 检查 GitHub CLI 是否已登录
if gh auth status &> /dev/null; then
    echo "✅ GitHub 身份验证成功"
else
    echo "🔑 请登录 GitHub..."
    gh auth login
fi

echo "📦 正在创建 GitHub 仓库..."
# 创建 GitHub 仓库
REPO_NAME="claude-code-builder"
gh repo create "$REPO_NAME" --public --description "🚀 AI-powered code generation platform - Lovable Clone with Claude API integration"

echo "🔗 正在添加远程仓库..."
# 添加远程仓库
git remote add origin "https://github.com/$(gh api user --jq .login)/$REPO_NAME.git"

echo "📤 正在推送代码到 GitHub..."
# 推送主分支
git push -u origin main

# 推送功能分支
git push -u origin feature/connect4-game

echo "🎯 正在创建 Pull Request..."
# 切换到功能分支并创建 PR
git checkout feature/connect4-game

# 创建 PR
gh pr create --title "feat: Add Connect 4 game demo and comprehensive documentation" --body "$(cat <<'EOF'
